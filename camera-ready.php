		    <h6>Detailed Instructions</h6>
                    
                    <ul>
                        <li>
			  The camera ready paper must be submitted by
			  May 10, 2013, 23:59 GMT at the
			  authors' console by logging into
			  the <a href="https://cmt.research.microsoft.com/MTAC2013/">MTAC
			  2013 CMT portal</a>. Do not delete any
			  previously submitted material.
                        </li>
			<br/>
                        <li>
			  To prepare the final manuscript, download
			  the template package
			  from <a href="files/templates/MTAC2013_template_Camera_ready.zip">here</a>. The package
			  includes an example pdf file containing
			  formatting and copyright tag
			  instructions. The camera-ready manuscript
			  must be submitted in <strong>pdf</strong> format only.
                        </li>
			<br/>
			
                        <li>
                          The camera-ready paper should include the
                          authors' names, their affiliations, and
                          contact details at the beginning of the
                          paper, as shown in the style file. You may
                          also refer to self-citations in the first
                          person, and include acknowledgements, if
                          any.
                        </li>
			<br/>
			<li>
			  The title of the paper must not be changed
			  for the camera ready version, unless it is to
			  correct some typographical errors, or the
			  change is suggested in the review process.

			  Any change must also be reflected in the
			  relevant CMT paper submission text record
			  that was created when the paper ID was
			  generated. 
			</li>
			<br/>
                        <li>
			  The corresponding author should be indicated
			  by an asterisk (*). In the provided
			  template, the first author is indicated as
			  the corresponding author. If the first
			  author is not the corresponding author,
			  please place the asterisk appropriately.
                        </li>
			<br/>
			<li>
                          The reviewers and area chairs have taken a
                          lot of effort to review each paper. Their
                          suggestions should be taken into account
                          while preparing the camera-ready paper.
                        </li>
			<br/>
                        <li>
			  The final manuscript should not
			  exceed <strong>3000</strong> words in
			  length. 
                        </li>
			<br/>
                        <li>
                          The camera-ready pdf file must
                          have <strong>all fonts
                          embedded</strong>. 
                          <p>
			  

                        </li>
			<br/>
			<li>
			  After creating the pdf file,
			  you <strong>must check</strong> if all fonts
			  are embedded and are only Type 1, Type 1C or TrueType. To verify,
