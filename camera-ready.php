			  Properties' and click on the 'Font'
			  tab. Make sure that all the fonts that are
			  listed have <strong>embedded subset</strong>
			  written next to them and have type label
			  as <strong>Type 1/Type 1C/TrueType</strong>.
			</li>
			<br/>
                        <li>
			  The final camera-ready pdf file should be
			  named as FirstAuthorLastName.PaperId.pdf.
                        </li>
